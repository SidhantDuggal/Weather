package com.climate.weather.utils;

public class ScreenDimens {
    private int height;
    private int width;
    public ScreenDimens(int height, int width){
        this.height = height;
        this.width = width;
    }
    public int getHeight(){
        return this.height;
    }
    public int getWidth(){
        return this.width;
    }
}
